def main():
    show_start_screen()
    room = 1
    hp = 100
    strength = 5

    while True:
        display_header(room, hp, strength)
        print(f"Tu atrodies alas telpńĀ nr. {room}.".center(TERMINAL_WIDTH))
        print('Komandas: explore / rest / advance / status / quit'.center(TERMINAL_WIDTH))
        cmd = input(Fore.GREEN + '> '.center(TERMINAL_WIDTH) + Style.RESET_ALL).strip().lower()

        if cmd == 'explore':
            damage = random.randint(5, 25)
            hp -= damage
            print(f'Tu sastapi briesmas un zaud\u0113ji {damage} HP.'.center(TERMINAL_WIDTH))
            time.sleep(1)
            if hp <= 0:
                display_header(room, 0, strength)
                show_game_over()
                break

        elif cmd == 'rest':
            heal = random.randint(8, 20)
            hp = min(100, hp + heal)
            print(f'Tu atp\u016bties un atguvi {heal} HP.'.center(TERMINAL_WIDTH))
            time.sleep(1)

        elif cmd == 'advance':
            room += 1
            print('Tu dodies dzi\u013ańĀk al\u0101...'.center(TERMINAL_WIDTH))
            time.sleep(1)
            if room > 4:
                display_header(room, hp, strength)
                show_victory()
                break

        elif cmd == 'status':
            # header will show status on next loop iteration
            pass

        elif cmd == 'quit':
            print('Izraujoties no alas. Uz redz\u0113\u0161anos!'.center(TERMINAL_WIDTH))
            break

        else:
            print('Neder\u012bga komanda.'.center(TERMINAL_WIDTH))

        time.sleep(0.6)


if __name__ == '__main__':
    main()
