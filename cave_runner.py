from colorama import Fore, Back, Style, init

init(autoreset=True)

TERMINAL_WIDTH = 80

def center_ascii(text):
    """Center ASCII art text line by line"""
    lines = text.split('\n')
    centered_lines = []
    for line in lines:
        # Remove trailing spaces to properly center
        line = line.rstrip()
        if line:
            # Center the line
            centered_lines.append(line.center(TERMINAL_WIDTH))
        else:
            centered_lines.append('')
    return '\n'.join(centered_lines)


def clear_screen():
    if os.name == 'nt':
        os.system('cls')
    else:
        os.system('clear')


def display_header(room, hp, strength):
    """Print a fixed header at the top with colors.

    Format: Istaba {room} | HP: [{hp_bar}] | Spńďks: {strength}
    HP is visualized with colored blocks.
    hp is expected between 0 and 100.
    """
    max_hp = 100
    hp_bar_length = 20
    hp_clamped = max(0, min(max_hp, int(hp)))
    filled = int((hp_clamped / max_hp) * hp_bar_length)
    
    # Color HP bar based on health
    if hp_clamped > 60:
        hp_color = Fore.GREEN
    elif hp_clamped > 30:
        hp_color = Fore.YELLOW
    else:
        hp_color = Fore.RED
    
    hp_bar = hp_color + '‚Ėą' * filled + Fore.WHITE + '‚ĖĎ' * (hp_bar_length - filled) + Style.RESET_ALL
    clear_screen()
    header = f"{Fore.CYAN}Istaba {room}{Style.RESET_ALL} | HP: [{hp_bar}] | {Fore.BLUE}Spńďks: {strength}{Style.RESET_ALL}"
    print(header.center(TERMINAL_WIDTH))
    print(Fore.CYAN + '-' * TERMINAL_WIDTH + Style.RESET_ALL)


